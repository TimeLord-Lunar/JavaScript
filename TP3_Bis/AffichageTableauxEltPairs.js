// Array Variables
let array = [1, 15, -3, 0, 8, 7, 4, -2, 28, 7, -1, 17, 2, 3, 0, 14, -4];

// Using a loop to display even elements of the array
for (let i = 0; i < array.length; i++) 
{
    // Check if the element is even
    if (array[i] % 2 === 0) 
    {
        console.log(array[i]);
    }
}
